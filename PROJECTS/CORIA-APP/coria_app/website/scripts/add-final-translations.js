function setNestedValue(obj, path, value) {
  const keys = path.split('.');
  let current = obj;

  for (let i = 0; i < keys.length - 1; i++) {
    const key = keys[i];
    if (!current[key] || typeof current[key] !== 'object') {
      current[key] = {};
    }
    current = current[key];
  }

  current[keys[keys.length - 1]] = value;
}

function addTranslations(locale, translations) {
  const filePath = path.join(__dirname, `../src/messages/${locale}.json`);
  const content = JSON.parse(fs.readFileSync(filePath, 'utf8'));

  Object.entries(translations).forEach(([key, value]) => {
    setNestedValue(content, key, value);
  });

  fs.writeFileSync(filePath, JSON.stringify(content, null, 2) + '\n', 'utf8');
  console.log(`✅ Added ${Object.keys(translations).length} translations to ${locale}.json`);
}

